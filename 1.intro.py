import turtle as t

t.pensize(5)
t.color('aqua')

t.penup()
t.goto(-250, 100)
t.pendown()

# The letter 'T'
t.forward(200)
t.backward(100)
t.right(90)
t.forward(200)


# Move to the next letter
t.penup()
t.back(200)
t.left(90)
t.forward(150)
t.pendown()
t.color("red")

# The letter 'I'
t.right(90)
t.forward(200)

# Move to the next letter
t.penup()
t.back(200)
t.left(90)
t.forward(50)
t.pendown()
t.color("green")


# The letter 'M'
t.right(90)
t.forward(200)
t.back(200)
t.left(30)
t.forward(150)
t.left(120)
t.forward(150)
t.right(150)
t.forward(200)

